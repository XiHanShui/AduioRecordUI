package com.example.administrator.aduiorecordui.playaudio;

/**
 * ClassName: SampleLine
 * Description:
 *
 * @author 彭赞
 * @version 1.0
 * @since 2017-12-14  14:26
 */
public class SampleLine {

    public float startX;
    public float startY;
    public float stopX;
    public float stopY;

    @Override
    public String toString() {
        return "SampleLine{" +
                "startX=" + startX +
                ", startY=" + startY +
                ", stopX=" + stopX +
                ", stopY=" + stopY +
                '}';
    }
}
